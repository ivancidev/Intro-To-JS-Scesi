//operadores numericos
console.log(6 >= 6);
console.log(6 === "6"); // ===

//operadores logicos
console.log(true && true);
console.log(true || true);
console.log(true || false);
console.log(false || false);

console.log(!false);
console.log((true && false) || false);

//operadores aritmeticos
console.log(5 + 3); 
console.log(5 - 3); 
console.log(5 * 3); 
console.log(5 / 3); 
console.log(2 ** 2); // otra forma de potencia

//operadores de texto
console.log("a" == "a"); 
